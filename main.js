// 3. 只在卫星底图显示的控制逻辑
map.on('baselayerchange', function(e) {
    if (e.layer === gaodeSatellite) {
        if (!map.hasLayer(bianjieLayer)) {
            bianjieLayer.addTo(map);
        }
    } else {
        if (map.hasLayer(bianjieLayer)) {
            map.removeLayer(bianjieLayer);
        }
    }
});

var bianjieLayer = L.geoJSON(null);

fetch("data/bianjie.json")
.then(function(res){
    return res.json();
})
.then(function(data){
    bianjieLayer.addData(data);
    bianjieLayer.addTo(map);

    // 获取原始研究区边界范围
    var originBounds = bianjieLayer.getBounds();
    
    // 向外整体扩大 0.15 度（宽松舒适尺度）
    var expand = 0.15;
    var newBounds = L.latLngBounds(
        [originBounds.getSouth() - expand, originBounds.getWest() - expand],
        [originBounds.getNorth() + expand, originBounds.getEast() + expand]
    );

    // 1. 打开自动全屏显示整片研究区
    map.fitBounds(originBounds, {padding:[15,15]});
    
    // 2. 锁定拖动范围，只能在扩边区域内滑动
    map.setMaxBounds(newBounds);

    console.log("✅ 研究区视角锁定，外围适度放宽");
})
.catch(function(err){
    console.error("边界加载错误",err);
});

// 点位随地图缩放自动调整大小
map.on('zoomend', function() {
    let z = map.getZoom();
    let scale = 1;

    if (z <= 9) {
        scale = 0.7;
    } else if (z <= 11) {
        scale = 1;
    } else if (z <= 13) {
        scale = 1.2;
    } else {
        scale = 1.5;
    }

    // 调整图标大小
    document.querySelectorAll(".leaflet-marker-icon").forEach(function(dom) {
        dom.style.transform = "scale(" + scale + ")";
        dom.style.transformOrigin = "center bottom";
    });

    // 调整文字大小
    document.querySelectorAll(".txtName").forEach(function(dom) {
        dom.style.fontSize = (12 * scale) + "px";
    });
});

