// 全局标记
let searchMarkList = [];
let singleTargetMark = null;

// 清空所有搜索标记
function clearAllSearchMark() {
  searchMarkList.forEach(m => map.removeLayer(m));
  searchMarkList = [];
  if(singleTargetMark){
    map.removeLayer(singleTargetMark);
    singleTargetMark = null;
  }
}

// 标记点击选中
function setMarkClick(mark, place) {
  mark.on("click", function(){
    clearAllSearchMark();
    singleTargetMark = L.marker([place.lat, place.lon]).addTo(map);
    map.setView([place.lat, place.lon], 11);
    document.getElementById("searchInput").value = place.name;
    document.getElementById("searchResult").style.display = "none";
  })
}

// 搜索按钮执行搜索
function startSearch() {
  let inp = document.getElementById("searchInput");
  let resBox = document.getElementById("searchResult");
  let key = inp.value.trim().toLowerCase();

  resBox.innerHTML = "";
  resBox.style.display = "none";
  clearAllSearchMark();

  if(!key) return;

  let allData = [...bigData, ...smallData, ...serviceData];
  let matchData = allData.filter(item=>item.name && item.name.toLowerCase().includes(key));

  if(matchData.length === 0){
    resBox.innerHTML = "<div class='search-item'>暂无相关地点</div>";
    resBox.style.display = "block";
    return;
  }

  // 地图批量亮标
  matchData.forEach(place=>{
    let m = L.marker([place.lat, place.lon]).addTo(map);
    searchMarkList.push(m);
    setMarkClick(m, place);
  })

  // 下拉列表
  matchData.forEach(place=>{
    let item = document.createElement("div");
    item.className = "search-item";
    item.innerText = place.name;
    item.onclick = function(){
      inp.value = place.name;
      resBox.style.display = "none";
      clearAllSearchMark();
      singleTargetMark = L.marker([place.lat, place.lon]).addTo(map);
      map.setView([place.lat, place.lon], 11);
    }
    resBox.appendChild(item);
  })
  resBox.style.display = "block";
}

// ========== 核心：输入框一清空 立刻自动消标 ==========
document.getElementById("searchInput").addEventListener("input", function(){
  // 只要输入框为空，直接清除所有定位
  if(this.value.trim() === ""){
    clearAllSearchMark();
    document.getElementById("searchResult").style.display = "none";
  }
})

