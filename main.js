// 地图初始化
var map = L.map('map').setView([33.8, 108.9], 8);

// 高德底图
var gaodeNormal = L.tileLayer(
  "https://webrd0{s}.is.autonavi.com/appmaptile?lang=zh_cn&size=1&scale=1&style=8&x={x}&y={y}&z={z}",
  { subdomains: ["1","2","3","4"] }
).addTo(map);

var gaodeSatellite = L.tileLayer(
  "https://webst0{s}.is.autonavi.com/appmaptile?style=6&x={x}&y={y}&z={z}",
  { subdomains: ["1","2","3","4"] }
);

var gaodeRoadLabel = L.tileLayer(
  "https://wprd0{s}.is.autonavi.com/appmaptile?lang=zh_cn&size=1&style=8&ltype=4&x={x}&y={y}&z={z}",
  { subdomains: ["1","2","3","4"] }
);

var satelliteWithRoad = L.layerGroup([gaodeSatellite, gaodeRoadLabel]);


// 图层组
var bigLayer = L.layerGroup();
var smallLayer = L.layerGroup();
var serviceLayer = L.layerGroup();
var isSatellite = false;

// 切换地图
function switchNormal() {
  map.removeLayer(satelliteWithRoad);
  map.addLayer(gaodeNormal);
  isSatellite = false;
  updateLayers();
}
function switchSatellite() {
  map.removeLayer(gaodeNormal);
  map.addLayer(satelliteWithRoad);
  isSatellite = true;
  updateLayers();
}
function updateLayers() {
  if (isSatellite) {
    bigLayer.addTo(map);
    smallLayer.addTo(map);
    serviceLayer.addTo(map);
  } else {
    map.removeLayer(bigLayer);
    map.removeLayer(smallLayer);
    map.removeLayer(serviceLayer);
  }
}

// 缩放显示小景点
map.on('zoomend', function() {
  if (map.getZoom() >= 12) {
    smallLayer.addTo(map);
  } else {
    map.removeLayer(smallLayer);
  }
});

